#include "ft_printf.h"

void	ft_putnbr2(unsigned int n)
{
	if (n >= 10)
	{
		ft_putnbr2(n / 10);
		ft_putnbr2(n % 10);
	}
	if (n <= 9)
	{
		ft_putchar_fd(n + 48);
	}
}
